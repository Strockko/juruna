package mcdonalds;

public class Bebida {
    private String nome ;
    private String preço ;
    private String codigo ;
    private String unidade_media ;
    private String quantidade ;
    
    
    public String Descrição_Oferta (String Descrição_Oferta) {
    
        return Descrição_Oferta;
    }
    
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getPreço() {
        return preço;
    }

    public void setPreço(String preço) {
        this.preço = preço;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getUnidade_media() {
        return unidade_media;
    }

    public void setUnidade_media(String unidade_media) {
        this.unidade_media = unidade_media;
    }

    public String getQuantidade() {
        return quantidade;
    }

    public void setQuantidade(String quantidade) {
        this.quantidade = quantidade;
    }
    
}
